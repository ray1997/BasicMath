public class MathOperations<T>
{
    public T Add(T a, T b)
    {
        dynamic x = a;
        dynamic y = b;
        return x + y;
    }

    public T Subtract(T a, T b)
    {
        dynamic x = a;
        dynamic y = b;
        return x - y;
    }

    public T Multiply(T a, T b)
    {
        dynamic x = a;
        dynamic y = b;
        return x * y;
    }

    public T Divide(T a, T b)
    {
        dynamic x = a;
        dynamic y = b;
        return x / y;
    }
}
